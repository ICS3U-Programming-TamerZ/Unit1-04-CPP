// This program does basic math.
#include <cmath>
#include <iostream>

int main() {
    std::cout << "9 + 2 = " << 9 + 2 << std::endl;
    std::cout << "7 - 3 = " << 7 - 3 << std::endl;
    std::cout << "10 / 2 = " << 10 / 2 << std::endl;
    std::cout << "8 - 4 / 2 = " << 8 - 4 / 2 << std::endl;
    std::cout << "2^3 = " << pow(2, 3) << std::endl;
    std::cout << "1 / 3 = " << float(1) / float(3) << std::endl;
}
